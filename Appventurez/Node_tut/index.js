// what is REPL in Node.JS. So, REPL stands for Read Eval Print Loop and
//  it represents a computer environment like a Windows console or Unix/Linux shell where a command
// is entered and the system responds with output in an interactive mode. Node.js or
//  Node comes bundled with a REPL environment. It performs the following tasks −

// Read − Reads the user's input, parses the input into JavaScript data-structure, and stores in memory.

// Eval − Takes and evaluates the data structure.

// Print − Prints the result.

// Loop − Loops the above command until the user presses ctrl-c twice.

// The REPL feature of Node is very useful in experimenting with Node.js codes and to debug JavaScript codes.



